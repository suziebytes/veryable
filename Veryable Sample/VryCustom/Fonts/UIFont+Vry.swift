import Foundation
import UIKit

extension UIFont {
    class func vryAvenirNextBold(_ size: CGFloat) -> UIFont {
        return UIFont(name: "AvenirNext-Bold", size: size)!
    }

    class func vryAvenirNextDemiBold(_ size: CGFloat) -> UIFont {
        return UIFont(name: "AvenirNext-DemiBold", size: size)!
    }

    class func vryAvenirNextDemiBoldItalic(_ size: CGFloat) -> UIFont {
        return UIFont(name: "AvenirNext-DemiBoldItalic", size: size)!
    }

    class func vryAvenirNextRegular(_ size: CGFloat) -> UIFont {
        return UIFont(name: "AvenirNext-Regular", size: size)!
    }

    class func vryAvenirNextMedium(_ size: CGFloat) -> UIFont {
        return UIFont(name: "AvenirNext-Medium", size: size)!
    }

    class func vryAvenirNextItalic(_ size: CGFloat) -> UIFont {
        return UIFont(name: "AvenirNext-Italic", size: size)!
    }
}
